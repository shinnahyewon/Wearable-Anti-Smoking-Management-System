void bluetooth_Event() {

    int i=0;
    char * pToken;
    char * pArray[ARR_CNT]={0};
    char recvBuf[CMD_SIZE]={0};
    char sendBuf[CMD_SIZE]={0};
    strcpy(recvBuf,btData);

    printf("btData : %s\r\n",btData);

    pToken = strtok(recvBuf,"[@]");
    while(pToken != NULL)
    {
        pArray[i] =  pToken;
        if(++i >= ARR_CNT)
          break;
        pToken = strtok(NULL,"[@]");
    }

    if(!strcmp(pArray[1],"GROUP1")) {
        // [LSB_BT]GROUP1 데이터 수신 시 실행
        int count1 = atoi(pArray[3]); // LSB Count 값을 정수로 변환
        int count2 = atoi(pArray[5]); // SHW Count 값을 정수로 변환
        if((count1 == 0) && (count2 == 0)) {
            // 성공 시 LCD에 출력
            move_cursor(0,0);
            lcd_string("LSB %d success!!",count1);
            move_cursor(1,0);
            lcd_string("SHW %d success!!",count2);
        } else if((count1 == 0) &&(count2 != 0)){
            // 실패 시 LCD에 출력
            move_cursor(0,0);
            lcd_string("LSB %d success!!",count1);
            move_cursor(1,0);
            lcd_string("SHW %d fail!!",count2);
        } else if((count1 != 0) && (count2 == 0)){
            // 실패 시 LCD에 출력
            move_cursor(0,0);
            lcd_string("LSB %d fail!!",count1);
            move_cursor(1,0);
            lcd_string("SHW %d success!!",count2);
        }
        else if((count1 != 0) && (count2 != 0)){
                   // 실패 시 LCD에 출력
            move_cursor(0,0);
            lcd_string("LSB %d fail!!",count1);
            move_cursor(1,0);
            lcd_string("SHW %d fail!!",count2);
               }
    } else if(!strcmp(pArray[1],"LED")) {
        // LED 제어 명령 수신 시 실행
        if(!strcmp(pArray[2],"ON")) {
            MX_GPIO_LED_ON(LD1_Pin|LD3_Pin|LD2_Pin);
            sprintf(sendBuf,"[%s]%s\n",pArray[0],pArray[1]);
        } else if(!strcmp(pArray[2],"OFF")) {
            MX_GPIO_LED_OFF(LD1_Pin|LD3_Pin|LD2_Pin);
            sprintf(sendBuf,"[%s]%s\n",pArray[0],pArray[1]);
        }
    } else if(!strncmp(pArray[1]," New conn",sizeof(" New conn"))) {
        // 블루투스 연결 시도 시 메시지 무시
        return;
    } else if(!strncmp(pArray[1]," Already log",sizeof(" Already log"))) {
        // 이미 연결된 경우 메시지 무시
        return;
    } else {
        // 다른 명령에 대한 처리를 추가할 수 있습니다.
        return;
    }

    // 라즈베리파이에 응답 메시지 전송
    sprintf(sendBuf,"[%s]%s@%s\n",pArray[0],pArray[1],pArray[2]);
    HAL_UART_Transmit(&huart2, (uint8_t *)sendBuf, strlen(sendBuf), 0xFFFF);
}


